//-------------------------------------------------------------------------*
// 函数名:  Delay()                                                        *
// 功  能:  延时                                                           *
// 参  数:  count(小于65535)                                               *
// 返  回:  无                                                             *
//-------------------------------------------------------------------------*
void Delay(uint16 count)
{
	uint16 i,j;
	for(j=0; j<count; j++)
	   for(i=0; i<35000; i++)
	      ;
}
